/*
 * isTmax - returns 1 if x is the maximum, two's codmplement number,
 *     and 0 otherwise
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x)
{
  int temp = x + 1; // 假设x是Tmax，这里+1后变成了Tmin
  x = x + temp;     // 这里变为了全1;
  x = ~x;           // x取反，变成了全0
  // 接下来排除-1,非Tmax返回1，则必须让-1返回0，就要把-1往0转换
  temp = !temp; // 假设temp是0，则!temp =1;如果temp是Tmin,则这里!temp变为0，所有非0的都是1
  x = x + temp;
  return !x;
}
/*
 * allOddBits - return 1 if all odd-numbered bits in word set to 1
 *   where bits are numbered from 0 (least significant) to 31 (most significant)
 *   Examples allOddBits(0xFFFFFFFD) = 0, allOddBits(0xAAAAAAAA) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allOddBits(int x)
{
  int b_8 = 0xAA;
  int b_16 = (b_8 << 8) | b_8;
  int b_32 = (b_16 << 16) | b_16;
  int equal = (x & b_32) ^ b_32; // a^a = 0，可用于判断是否相等
  return !equal;
}
