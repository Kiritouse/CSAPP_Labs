/*
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x)
{
  int a = !(x >> 4 ^ 0x3); // 判断前4位是不是相同
  int b = x & 0xF;         // 保存后4位的数据
  int c = ~(0xA) + 1;      //-A
  int e = 0x80 << 8;       // 扩展位数
  int d = !!(b + c) & e;   // 两个！！作用是把所有非0的值变为1，0还是0，！的话0就会变成1
  // 用!的话如果为正数，即为0时，返回的值就为1，明显不对
  return a & d;
}
/*
 * conditional - same as x ? y : z
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z)
{
  int temp = !!x;            // a如果不为0的时候，temp = 1;
  int condition = ~temp + 1; // temp = -1,二进制表示就全是1
  return (condition & y) | (~condition & z);
}
/*
 * isLessOrEqual - if x <= y  then return 1, else return 0
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y)
{
  int negX = ~x + 1;                     //-x
  int ySubX = y + negX;                  // y-x;
  int checkSign_ySubX = ySubX >> 31 & 1; // y-x的 符号
  // 获取x和y的符号
  int leftSign = 1 << 31; // 符号位为1的二进制
  int xSign = x & leftSign;
  int ySign = y & leftSign;
  int signBitXor = xSign ^ ySign;
  signBitXor = (signBitXor >> 31) & 1;
  return (!signBitXor) & (!checkSign_ySubX) | (signBitXor & (xSign >> 31));
  return 2;
}
